package com.usel.app.model;

public class Vendor {
	   private int id;
	   private String name;
	   
	   public int getId() {
	      return id;
	   }
	   
	   public void setId(int id) {
	      this.id = id;
	   }
	   
	   public String name() {
	      return name;
	   }
	   
	   public void name(String name) {
	      this.name = name;
	   }
}